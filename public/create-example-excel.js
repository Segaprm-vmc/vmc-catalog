// Скрипт для создания примера Excel файла
// Запускается в Node.js: node create-example-excel.js

const XLSX = require('xlsx');

// Данные для примера
const data = [
  ['Название характеристики', 'Значение'],
  ['Двигатель', '4-тактный, одноцилиндровый'],
  ['Объем двигателя', '250 куб.см'],
  ['Мощность', '25 л.с.'],
  ['Максимальная скорость', '140 км/ч'],
  ['Расход топлива', '3.5 л/100км'],
  ['Вес', '140 кг'],
  ['Высота по седлу', '800 мм'],
  ['Объем топливного бака', '12 л'],
  ['Коробка передач', '6-ступенчатая'],
  ['Тип охлаждения', 'Воздушное'],
  ['Система зажигания', 'Электронная'],
  ['Тормоза передние', 'Дисковые 300мм'],
  ['Тормоза задние', 'Дисковые 240мм'],
  ['Шины передние', '110/70-17'],
  ['Шины задние', '140/70-17']
];

// Создаем workbook и worksheet
const wb = XLSX.utils.book_new();
const ws = XLSX.utils.aoa_to_sheet(data);

// Добавляем worksheet в workbook
XLSX.utils.book_append_sheet(wb, ws, 'Характеристики');

// Сохраняем файл
XLSX.writeFile(wb, 'example_specs.xlsx');

console.log('Файл example_specs.xlsx создан успешно!'); 